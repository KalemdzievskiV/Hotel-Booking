package com.example.hotel_management.enums;

/**
 * Enum representing different types of notifications that users can subscribe to.
 */
public enum NotificationType {
    PRICE_CHANGE,       // Notifications for room price changes
    AVAILABILITY,       // Notifications for room availability
    PROMOTIONS,         // Notifications for hotel promotions and special offers
    BOOKING_REMINDER,   // Reminders for upcoming bookings
    HOTEL_NEWS,        // General hotel news and updates
    MAINTENANCE        // Notifications about hotel/room maintenance
}
